# Программа Орел и решка
# Демонстрирует выбор случайного числа и работу цикла

import random

# Инициализируем счетчики
heads = 0
tails = 0

# Общее количество подбрасываний
total_flips = 100

# Счетчик итераций
counter = 0

# Цикл подбрасывания монетки
while counter < total_flips:
  # Генерируем случайное число: 0 или 1
  coin = random.randint(0, 1)

  # Проверяем результат
  if coin== 0:
    heads += 1
  else:
    tails += 1

  # Увеличиваем счетчик итераций
  counter += 1

# Выводим результаты
print(f"\nКоличество подбрасываний: {total_flips}")
print(f"Количество орлов: {heads}")
print(f"Количество решек: {tails}")
